<script>
  import { app } from '$lib/state.svelte.js';
  import { normalizeFigmaProps, normalizeWebProps, compareProperties } from '$lib/property-utils.js';

  const figmaNorm = $derived(normalizeFigmaProps(app.figmaProperties));
  const webNorm = $derived(normalizeWebProps(app.webProperties));
  const comparison = $derived(
    figmaNorm || webNorm ? compareProperties(figmaNorm, webNorm) : null
  );

  let expandedCats = $state({});
  let excluded = $state({});

  function toggleCat(key) {
    expandedCats[key] = !expandedCats[key];
  }

  function expandAll() {
    if (!comparison) return;
    for (const cat of comparison.categories) expandedCats[cat.key] = true;
  }

  function collapseAll() {
    if (!comparison) return;
    for (const cat of comparison.categories) expandedCats[cat.key] = false;
  }

  function toggleProp(prop) {
    excluded[prop] = !excluded[prop];
  }

  function toggleCategory(cat) {
    const allExcluded = cat.rows.every(r => excluded[r.prop]);
    for (const r of cat.rows) excluded[r.prop] = !allExcluded;
  }

  function isCatExcluded(cat) {
    return cat.rows.every(r => excluded[r.prop]);
  }

  function isCatPartial(cat) {
    return cat.rows.some(r => excluded[r.prop]) && !isCatExcluded(cat);
  }

  let copyMsg = $state('');

  // Filtered score based on non-excluded properties
  const filtered = $derived.by(() => {
    if (!comparison) return { matched: 0, total: 0, categories: [] };
    let matched = 0, total = 0;
    const categories = comparison.categories.map(cat => {
      const activeRows = cat.rows.filter(r => !excluded[r.prop]);
      const catMatched = activeRows.filter(r => r.match === true).length;
      const catTotal = activeRows.length;
      matched += catMatched;
      total += catTotal;
      return { ...cat, matched: catMatched, total: catTotal, activeRows };
    });
    return { matched, total, categories };
  });

  function buildMarkdown() {
    if (!comparison || !filtered) return '';
    const pct = filtered.total > 0 ? Math.round(filtered.matched / filtered.total * 100) : 0;
    const icon = pct >= 80 ? '🟢' : pct >= 50 ? '🟡' : '🔴';
    let md = `## ${icon} Loupe Inspect — ${pct}% match (${filtered.matched}/${filtered.total})\n\n`;

    for (const cat of comparison.categories) {
      const activeRows = cat.rows.filter(r => !excluded[r.prop]);
      if (activeRows.length === 0) continue;

      const catMatched = activeRows.filter(r => r.match === true).length;
      md += `### ${cat.label} (${catMatched}/${activeRows.length})\n\n`;
      md += `| Property | Figma | Web | Match |\n`;
      md += `|----------|-------|-----|-------|\n`;

      for (const row of activeRows) {
        const status = row.match === true ? '✅' : row.match === false ? '❌' : '➖';
        const f = row.figma ? `\`${row.figma}\`` : '—';
        const w = row.web ? `\`${row.web}\`` : '—';
        md += `| \`${row.prop}\` | ${f} | ${w} | ${status} |\n`;
      }
      md += '\n';
    }

    md += `---\n*Generated by [Loupe](https://github.com/TitaniumCladStudios/loupe)*\n`;
    return md;
  }

  function buildPlainText() {
    if (!comparison || !filtered) return '';
    const pct = filtered.total > 0 ? Math.round(filtered.matched / filtered.total * 100) : 0;
    let txt = `Loupe Inspect — ${pct}% match (${filtered.matched}/${filtered.total})\n`;
    txt += '='.repeat(50) + '\n\n';

    for (const cat of comparison.categories) {
      const activeRows = cat.rows.filter(r => !excluded[r.prop]);
      if (activeRows.length === 0) continue;

      const catMatched = activeRows.filter(r => r.match === true).length;
      txt += `${cat.label} (${catMatched}/${activeRows.length})\n`;
      txt += '-'.repeat(40) + '\n';

      const propW = Math.max(...activeRows.map(r => r.prop.length), 8);
      for (const row of activeRows) {
        const status = row.match === true ? '[OK]' : row.match === false ? '[!!]' : '[--]';
        const f = row.figma || '—';
        const w = row.web || '—';
        txt += `  ${status} ${row.prop.padEnd(propW)}  Figma: ${f}  |  Web: ${w}\n`;
      }
      txt += '\n';
    }
    return txt;
  }

  async function copyMarkdown() {
    await navigator.clipboard.writeText(buildMarkdown());
    copyMsg = 'Markdown copied!';
    setTimeout(() => copyMsg = '', 2000);
  }

  async function copyPlainText() {
    await navigator.clipboard.writeText(buildPlainText());
    copyMsg = 'Plain text copied!';
    setTimeout(() => copyMsg = '', 2000);
  }
</script>

