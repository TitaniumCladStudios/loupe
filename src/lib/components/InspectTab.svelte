              title={isCatExcluded(rawCat) ? 'Include all ' + rawCat.label + ' properties in score' : 'Exclude all ' + rawCat.label + ' properties from score'}
              class="cat-check"
            />
            <button class="cat-toggle" onclick={() => toggleCat(rawCat.key)}>
              <span class="cat-arrow">{expandedCats[rawCat.key] ? '▼' : '▶'}</span>
              <span class="cat-label">{rawCat.label}</span>
            </button>
            <span class="cat-summary">
              <span class="cat-count">{cat.matched}/{cat.total}</span>
              {#if cat.total === 0}
                <span class="cat-badge excluded">Excluded</span>
              {:else if cat.matched === cat.total}
                <span class="cat-badge good">All match</span>
              {:else}
                <span class="cat-badge warn">{cat.total - cat.matched} differ</span>
              {/if}
            </span>
          </div>

          {#if expandedCats[rawCat.key]}
            <div class="cat-table">
              <div class="table-header">
                <span class="col-check"></span>
                <span class="col-prop">Property</span>
                <span class="col-val">Figma</span>
                <span class="col-val">Web</span>
                <span class="col-match"></span>
              </div>
              {#each rawCat.rows as row}
                {@const isExcluded = excluded[row.prop]}
                <div
